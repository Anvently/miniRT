#include <libft.h>

/// @brief Check if char is lowercase and convert it to uppercase.
/// @param c Ascii character to convert
/// @return Uppercase equivalent of c if exist
int	ft_toupper(int c)
{
	if (c >= 'a' && c <= 'z')
		return (c - ('a' - 'A'));
	else
		return (c);
}
