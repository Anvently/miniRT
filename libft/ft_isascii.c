#include <libft.h>

/// @brief Check if c fits into ascii table [0-127]
/// @param c Character to check
/// @return Non-zero if true, zero otherwise
int	ft_isascii(int c)
{
	if (c >= 0 && c <= 127)
		return (1);
	return (0);
}
