#include <libft.h>

/// @brief Check digit character [0-9]
/// @param c Character to check
/// @return Non-zero if true, zero otherwise
int	ft_isdigit(int c)
{
	if (c >= '0' && c <= '9')
		return (1);
	return (0);
}
