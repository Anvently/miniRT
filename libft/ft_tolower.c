#include <libft.h>

/// @brief Check if char is uppercase and convert it to lowercase.
/// @param c Ascii character to convert
/// @return Lowercase equivalent of c if exist
int	ft_tolower(int c)
{
	if (c >= 'A' && c <= 'Z')
		return (c + ('a' - 'A'));
	else
		return (c);
}
