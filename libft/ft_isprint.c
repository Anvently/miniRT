#include <libft.h>

/// @brief Check if c is a printable character. Include space.
/// @param c Character to check
/// @return Non-zero if true, zero otherwise
int	ft_isprint(int c)
{
	if (c >= 32 && c <= 126)
		return (1);
	return (0);
}
